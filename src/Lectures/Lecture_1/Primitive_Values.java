package Lectures.Lecture_1;

public class Primitive_Values {
    public static void main(String[] args){

        /* STRING is NOT a primitive value */

        /* PRIMITIVE VALUES */
        int rollno = 64;
        char name = 'R';
        float marks = 64.64f;                        // Made from DOUBLE use 'f'
        long LongIntegerValues = 45785412659L;      // Made from DOUBLE use 'L'
        double LongDecimalValues = 4521657.1264318;
        boolean check = true;

        /* Outputs */
        System.out.println("Integer value is : "+rollno);
        System.out.println("Character is : "+name);
        System.out.println("Float values is :"+marks);
        System.out.println("Long value is :"+LongIntegerValues);
        System.out.println("Double value is :"+LongDecimalValues);
        System.out.println("True of False : "+check);
    }
}
